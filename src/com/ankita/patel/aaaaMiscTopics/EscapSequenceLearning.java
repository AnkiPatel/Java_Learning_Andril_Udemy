package com.ankita.patel.aaaaMiscTopics;

public class EscapSequenceLearning {
	
	public static void main(String[] args) {
		
		System.out.println("===Escape sequences===");
		System.out.println("My favourite book is \"The one thing\" by Gary Keller.\n");
		System.out.println("Path to the source code: D\\Java\\sources");
		
		/*
	 	\t - tab.
		\b - backspace (a step backward in the text or deletion of a single character).
		\n - new line.
		\r - carriage return. ()
		\f - form feed.
		\' single quote.
		\" double quote.
		\\ backslash.
		 */
		
		//Print unicode
		System.out.println("Unicode characters, \u00A9 IT-Bulls.com");
	}

}

